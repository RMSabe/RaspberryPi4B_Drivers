#ifndef BCM2711_GPCLK_REGISTER_MAPPING_H
#define BCM2711_GPCLK_REGISTER_MAPPING_H

/*
 * BCM2711 GPCLK Control registers are 32bit registers.
 * These registers have 3 addressing modes:
 * 32bit addressing (GPCLK_ADDR_32BIT), 35bit addressing (GPCLK_ADDR_35BIT) and Low Peripheral 32bit addressing (GPCLK_ADDR_LPMODE).
 *
 * Addressing mode must be defined before including this header.
 */

#ifdef GPCLK_ADDR_32BIT
#define GPCLK_BASE_ADDR 0x7E101000
#define GPCLK0_CTRL_REG_ADDR 0x7E101070
#define GPCLK0_DIV_REG_ADDR 0x7E101074
#define GPCLK1_CTRL_REG_ADDR 0x7E101078
#define GPCLK1_DIV_REG_ADDR 0x7E10107C
#define GPCLK2_CTRL_REG_ADDR 0x7E101080
#define GPCLK2_DIV_REG_ADDR 0x7E101084
#endif //GPCLK_ADDR_32BIT

#ifdef GPCLK_ADDR_35BIT
#define GPCLK_BASE_ADDR 0x47E101000
#define GPCLK0_CTRL_REG_ADDR 0x47E101070
#define GPCLK0_DIV_REG_ADDR 0x47E101074
#define GPCLK1_CTRL_REG_ADDR 0x47E101078
#define GPCLK1_DIV_REG_ADDR 0x47E10107C
#define GPCLK2_CTRL_REG_ADDR 0x47E101080
#define GPCLK2_DIV_REG_ADDR 0x47E101084
#endif //GPCLK_ADDR_35BIT

#ifdef GPCLK_ADDR_LPMODE
#define GPCLK_BASE_ADDR 0xFE101000
#define GPCLK0_CTRL_REG_ADDR 0xFE101070
#define GPCLK0_DIV_REG_ADDR 0xFE101074
#define GPCLK1_CTRL_REG_ADDR 0xFE101078
#define GPCLK1_DIV_REG_ADDR 0xFE10107C
#define GPCLK2_CTRL_REG_ADDR 0xFE101080
#define GPCLK2_DIV_REG_ADDR 0xFE101084
#endif //GPCLK_ADDR_LPMODE

#define GPCLK_MAPPING_SIZE_BYTES 136

#define GPCLK_MAPPING_BYTEP_SIZE 136

#define GPCLK0_CTRL_BYTEP_POS 112
#define GPCLK0_DIV_BYTEP_POS 116
#define GPCLK1_CTRL_BYTEP_POS 120
#define GPCLK1_DIV_BYTEP_POS 124
#define GPCLK2_CTRL_BYTEP_POS 128
#define GPCLK2_DIV_BYTEP_POS 132

#define GPCLK_MAPPING_UINTP_SIZE 34

#define GPCLK0_CTRL_UINTP_POS 28
#define GPCLK0_DIV_UINTP_POS 29
#define GPCLK1_CTRL_UINTP_POS 30
#define GPCLK1_DIV_UINTP_POS 31
#define GPCLK2_CTRL_UINTP_POS 32
#define GPCLK2_DIV_UINTP_POS 33

#endif
